<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.1 Definition and Purpose</button>
<div class='accordion-content'><p>Computational thinking is a structured method of solving complex problems by breaking them down, identifying patterns, focusing on essential information, and designing a clear set of steps to reach a solution.
Its purpose is to create logical, efficient solutions that can be carried out by a computer or a person</p></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.2 When to Use</button>
<div class='accordion-content'><p>Use computational thinking when a problem is complex, unclear, or too large to solve all at once.
It is helpful when a task requires logical steps, automation, or the creation of an efficient, repeatable solution</p></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.3 Benefits and Drawbacks</button>
<div class='accordion-content'><p>Benefits:</br>

Helps simplify complex problems</br>
Leads to organised, logical solutions</br>
Makes solutions easier to test and reuse</br>
Works across multiple subjects, not only computing</br>
</p>
<p>
Drawbacks:</p>

Can be time‑consuming for simple tasks</br>
Requires practice and understanding</br>
May oversimplify a problem if not used carefully</br>
Not all problems fit well with computational thinking</p></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.4 Components</button>
<div class='accordion-content'><p>Computational thinking is made up of four core skills:
</p>
Decomposition – breaking a big problem into smaller parts</br>
Pattern Recognition – spotting trends or similarities</br>
Abstraction – removing unnecessary details</br>
Algorithmic Design – creating step‑by‑step instruction</div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.5 Components Pros/Cons</button>
<div class='accordion-content'><p>Benefits:</p>

Breaks down complexity</br>
Helps reuse existing solutions</br>
Reduces confusion</br>
Allows clear, repeatable processes</br>

<p>Drawbacks:</p>

Some problems are hard to break down</br>
Patterns are not always easy to spot</br>
Abstraction risks hiding important details</br>
Algorithms may require many revisions</br></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.6 Purpose of Decomposition</button>
<div class='accordion-content'><p>Decomposition helps make difficult problems easier to understand by dividing them into smaller, more manageable pieces that can be solved one at a time.</p></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.7 Tasks of Decomposition</button>
<div class='accordion-content'><p>Decomposition involves:</p>

Identifying the main parts of a problem</br>
Understanding what each part involves</br>
Breaking the problem into smaller chunks</br>
Breaking solutions into steps that work together</br></div>
</div>
<div class='accordion-item'>
<button class='accordion-header' type='button'>1.1.8 Using Decomposition</button>
<div class='accordion-content'><p>This means taking a real problem, dividing it into smaller tasks, and solving each task individually to create the overall solution.</p></div>
</div>
