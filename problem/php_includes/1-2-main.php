<button class='accordion-header' type='button'>1.2.1 Definition, Characteristics and Purpose of Algorithms</button>
<div class='accordion-content'><p>An algorithm is a precise, step-by-step set of instructions designed to solve a problem or complete a task. Algorithms have characteristics such as being clear, finite, and ordered. Their purpose is to create reliable, repeatable solutions that can be followed by humans or computers.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.2 Methods to Express Algorithms</button>
<div class='accordion-content'><p>Algorithms can be expressed using flowcharts or written descriptions. Flowcharts use symbols such as terminators, processes, sub-processes, decisions, inputs/outputs, arrows, and labels. Written descriptions use hierarchical markers to show sequence clearly.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.3 Benefits and Drawbacks of Flowcharts</button>
<div class='accordion-content'><p>Flowcharts visually represent algorithms, making them easy to understand and follow. However, they can become complex for large algorithms and are time-consuming to update.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.4 Benefits and Drawbacks of Written Descriptions</button>
<div class='accordion-content'><p>Written descriptions are flexible and easy to modify. They suit detailed explanations but may be harder to visualise and can cause confusion without clear structure.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.5 Actions to Control Ordering of Steps</button>
<div class='accordion-content'><p>Algorithms use sequence (steps in order), selection (decision-making), and iteration (repetition of steps) to control how tasks are executed.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.6 Determine the Purpose of an Algorithm</button>
<div class='accordion-content'><p>This involves analysing an algorithm to understand what it is designed to achieve and how it functions.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.7 Determine the Output of an Algorithm</button>
<div class='accordion-content'><p>Given an input, you should be able to trace the steps of the algorithm to accurately determine the final output.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.8 Identify Errors in an Algorithm</button>
<div class='accordion-content'><p>Error identification involves spotting logical, structural, or sequencing issues that prevent the algorithm from working correctly.</p></div>
</div>

<div class='accordion-item'>
<button class='accordion-header' type='button'>1.2.9 Correct Errors in an Algorithm</button>
<div class='accordion-content'><p>This means fixing mistakes in logic, flow, or structure to ensure the algorithm behaves as intended.</p></div>
</div>

