<h1><i class="fas fa-user-secret"></i> 8.2.2 Technical Vulnerabilities</h1>
    
<section>
        <h2>Understand potential vulnerabilities, including prevention and mitigation methods</h2>
        <p>Click each category below to expand and view prevention/mitigation steps:</p>
  <div class="accordion-item">
    <button class="accordion-header" type="button">Inadequate Security Processes</button>
        <div class="accordion-content">
          <ul>
            <li><strong>Weak encryption</strong> — attackers can more easily break or intercept data if low‑strength or outdated algorithms are used.</li>
            <li><strong>Inadequate password policy</strong> — short, reused, or simple passwords allow quick compromise through guessing or brute‑force attacks.</li>
            <li><strong>Failure to use multi‑factor authentication (MFA)</strong> — relying only on passwords increases the chance of unauthorised access.</li>
          </ul>
        </div>
      </div>

  <div class="accordion-item">
    <button class="accordion-header" type="button">Out‑of‑Date Components</button>
    <div class="accordion-content">
      <ul>
        <li><strong>Hardware vulnerabilities</strong> — older devices may lack security features, updated chip firmware, or modern protection mechanisms.</li>
        <li><strong>Out‑of‑date software</strong> — unsupported operating systems and applications cannot receive patches, leaving them exposed to:
          <ul>
            <li>Compatibility issues with newer systems</li>
            <li>Known unpatched vulnerabilities</li>
            <li>Zero‑day exploits that target old software</li>
          </ul>
        </li>
        <li><strong>Firmware vulnerabilities</strong> — outdated BIOS/UEFI, router firmware, and device firmware can be exploited to gain deep‑level access.</li>
      </ul>
    </div>
  </div>
<p class="note"><em>Good practice:</em> keep systems patched, update hardware life‑cycle plans, enforce MFA, and regularly review encryption and authentication policies.</p>
</div>




</section>