      <button class="accordion-header" aria-expanded="false">Malicious hacking</button>
      <div class="accordion-content" hidden>
        <ul>
          <li><i class="Strong">Hacktivists</i>  Actors motivated by ideology; may deface or disrupt services to make a statement.</li>
          <li><i class="Strong">Nation states</i>  State-sponsored groups conducting espionage, sabotage or intelligence gathering.</li>
          <li><i class="Strong">Organised crime</i>  Professional cybercriminals focused on financial gain and large-scale fraud.</li>
          <li><i class="Strong">Individuals</i>  Lone actors exploring, vandalising, or exploiting systems for various motives.</li>
          <li><i class="Strong">Password cracking</i>  Automated brute-force or dictionary attacks to recover or guess user passwords.</li>
          <li><i class="Strong">Cross-site scripting (XSS)</i>  Injected scripts run in victims' browsers to steal data or hijack sessions.</li>
          <li><i class="Strong">SQL injection</i>  Malicious SQL input used to read, modify or delete data from a backend database.</li>
          <li><i class="Strong">Buffer overflow</i>  Exploiting memory bugs to overwrite memory and execute arbitrary code.</li>
        </ul>
      </div>
    </div>

    <div class="accordion-item">
      <button class="accordion-header" aria-expanded="false">Malware</button>
      <div class="accordion-content" hidden>
        <ul>
          <li><i class="Strong">Viruses</i>  Malicious code that attaches to files and spreads when the host file is executed.</li>
          <li><i class="Strong">Worms</i>  Self-replicating malware that spreads across networks without user interaction.</li>
          <li><i class="Strong">Key loggers</i>  Software that records keystrokes to capture passwords and sensitive input.</li>
          <li><i class="Strong">Ransomware</i>  Encrypts files or systems and demands payment in exchange for a decryption key.</li>
          <li><i class="Strong">Spyware</i>  Covertly collects data about a userâ€™s activities and transmits it to attackers.</li>
          <li><i class="Strong">Remote access trojans (RATs)</i>  Provide attackers with persistent remote control over infected machines.</li>
        </ul>
      </div>
    </div>

    <div class="accordion-item">
      <button class="accordion-header" aria-expanded="false">Social engineering</button>
      <div class="accordion-content" hidden>
        <ul>
          <li><i class="Strong">Phishing</i>  Mass fraudulent emails or websites that trick users into revealing credentials or clicking malicious links.</li>
          <li><i class="Strong">Spear phishing</i>  Targeted phishing aimed at a specific person or organisation using personalised information to increase success.</li>
          <li><i class="Strong">Smishing</i>  Phishing via SMS/text messages that direct victims to malicious links or request sensitive information.</li>
          <li><i class="Strong">Vishing</i>  Voice phishing over phone calls where attackers impersonate trusted parties to extract information.</li>
          <li><i class="Strong">Pharming</i>  Redirecting users from legitimate websites to malicious ones (DNS poisoning or hosts-file manipulation).</li>
          <li><i class="Strong">Watering hole attacks</i>  Compromising websites frequented by a target group to infect visitors with malware.</li>
          <li><i class="Strong">USB baiting</i>  Leaving infected USB drives in public places to entice users to plug them in and run malware.</li>
        </ul>
      </div>
    </div>

