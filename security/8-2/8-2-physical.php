    <div class="accordion-item">
      <button class="accordion-header">
        <i class="strong"></i> Poor Access Control
      </button>
      <div class="accordion-content">
        <ul>
          <li><i class="strong">Do not allow tailgating:</i> Prevent unauthorized individuals from following staff into secure areas.</li>
          <li><i class="strong">Use complex access codes:</i> Simple codes are easy to guess and compromise security.</li>
          <li><i class="strong">Change codes regularly:</i> Reduces risk of old codes being exploited.</li>
          <li><i class="strong">Monitor access areas:</i> Surveillance helps detect suspicious activity.</li>
          <li><i class="strong">Audit staff access:</i> Regular reviews ensure compliance and detect anomalies.</li>
        </ul>
      </div>
    </div>

